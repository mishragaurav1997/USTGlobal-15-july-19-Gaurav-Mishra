public class AssignmentOperators{
public static void main(String args[]){
	 int i,j;
	 i = 10;
	 j = 20;
	 System.out.println("i = " +i);
	 System.out.println("j = " +j);
	 j += i;
System.out.println("j += " +j);
j -= i;
System.out.println("j -= " +j);
j *= i;
System.out.println("j *= " +j);
j /= i;
System.out.println("j /= " +j);
j %= i;
System.out.println("j %= " +j);
}
}
	 